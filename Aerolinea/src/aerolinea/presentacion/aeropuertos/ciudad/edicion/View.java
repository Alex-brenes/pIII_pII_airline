    private Model model;
    private Controller controller;

    public Model getModel() {
        return model;
    }

    public void setModel(Model model) {
        this.model = model;
        this.model.addObserver(this);
    }

    public Controller getController() {
        return controller;
    }

    public void setController(Controller controller) {
        this.controller = controller;
    }

    @Override
    public void update(Observable o, Object arg) {
        this.renderCiudad(this.model.getCiudad());
    }

    private Ciudad toCiudad(){
        Ciudad c = new Ciudad();
        c.setNombre((this.jTextFieldNombre.getText()));
        c.setPais((Pais)this.jComboBoxPaises.getSelectedItem());
        return c;
    }
    
    private void renderCiudad(Ciudad c) {
        this.jTextFieldNombre.setText(c.getNombre());
        this.jComboBoxPaises.setModel(new DefaultComboBoxModel<Pais>(
                this.model.getPaises().toArray(new Pais[0])));
        this.jComboBoxPaises.setSelectedItem(c.getPais());
    }

